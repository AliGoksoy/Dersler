using System;
using System.Collections.Generic;
using System.Linq;
using System.Security.Cryptography;
using System.Text;
using System.Threading.Tasks;

namespace Metodlar
{
    class Toplama
    {

        public int Topla(int s1, int s2)
        {
            return s1 + s2;
        }

        public int Topla(int s1, int s2, int s3)
        {
            return s1 + s2 + s3;
        }
    }
}
